blazegears.formatting.tests.dateFormattingTest = function() {
	var date;
	var formatter = new blazegears.formatting.DateFormatter();
	var i;
	var new_full_names;
	var new_short_names;
	var original_names;
	
	// using utc
	formatter.setTimeZoneOffset(blazegears.formatting.TimeZoneOffset.UTC);
	
	// months
	original_names = formatter.getFullMonthNames();
	new_short_names = [];
	new_full_names = [];
	for (i = 0; i < 12; ++i)
	{
		new_full_names.push("a" + i.toString());
		new_short_names.push("b" + i.toString());
	}
	formatter.setFullMonthNames(new_full_names);
	formatter.setShortMonthNames(new_short_names);
	for (i = 0; i < 12; ++i) {
		date = new Date(30 * 86400000 * i + 15 * 86400000 + 43200000);
		strictEqual(formatter._getMonthName(date), new_full_names[i], "Set " + original_names[i] + "'s full name.");
		strictEqual(formatter._getAbbreviatedMonthName(date), new_short_names[i], "Set " + original_names[i] + "'s abbreviated name.");
	}
	
	// days
	original_names = formatter.getFullDayNames();
	new_short_names = [];
	new_full_names = [];
	for (i = 0; i < 7; ++i)
	{
		new_full_names.push("c" + i.toString());
		new_short_names.push("d" + i.toString());
	}
	formatter.setFullDayNames(new_full_names);
	formatter.setShortDayNames(new_short_names);
	for (i = 0; i < 7; ++i) {
		date = new Date(86400000 * (i + 3) + 43200000);
		strictEqual(formatter._getDayName(date), new_full_names[i], "Set " + original_names[i] + "'s full name.");
		strictEqual(formatter._getAbbreviatedDayName(date), new_short_names[i], "Set " + original_names[i] + "'s abbreviated name.");
	}
	
	// ordinal suffixes
	original_names = formatter.getOrdinalSuffixes();
	new_full_names = [];
	for (i = 0; i < 31; ++i)
	{
		new_full_names.push("e" + i.toString());
	}
	formatter.setOrdinalSuffixes(new_full_names);
	for (i = 0; i < 31; ++i) {
		date = new Date(86400000 * i + 43200000);
		strictEqual(formatter._getOrdinalSuffix(date), new_full_names[i], "Set the ordinal suffix for the " + (i + 1) + original_names[i] + ".");
	}
	
	// meridiems
	original_names = formatter.getShortMeridiemNames();
	new_short_names = [];
	for (i = 0; i < 2; ++i)
	{
		new_short_names.push("f" + i.toString());
	}
	formatter.setShortMeridiemNames(new_short_names);
	for (i = 0; i < 2; ++i) {
		date = new Date(i * 28800000 + 28800000);
		strictEqual(formatter._getLowerCaseMeridiem(date), new_short_names[i], "Set the lowercase " + original_names[i] + ".");
		strictEqual(formatter._getUpperCaseMeridiem(date), new_short_names[i].toUpperCase(), "Set the uppercase " + original_names[i] + ".");
	}
}
test("Date Formatting", blazegears.formatting.tests.dateFormattingTest);

blazegears.formatting.tests.numberFormattingTest = function() {
	var DecimalVisibility = blazegears.formatting.DecimalVisibility;
	var formatter = new blazegears.formatting.NumberFormatter();
	
	formatter.enableLeadingZero(true);
	formatter.setDecimalDelimiter(".");
	formatter.setDecimalPrecision(2);
	formatter.setDecimalVisibility(DecimalVisibility.FULL_PRECISION);
	formatter.getGroupDelimiter(",");
	formatter.setGroupSize(3);
	formatter.setNegativePrefix("-");
	formatter.setNegativeSuffix("");
	formatter.setRoundingCallback(Math.round);
	
	// non-number values
	strictEqual(formatter.formatNumber("1"), "1.00", "Try formatting an integer string value.");
	strictEqual(formatter.formatNumber("1.11"), "1.11", "Try formatting a decimal string value.");
	strictEqual(formatter.formatNumber("z"), "0.00", "Try formatting a NaN value.");
	
	// decimal visibility
	formatter.setDecimalVisibility(DecimalVisibility.FULL_PRECISION);
	strictEqual(formatter.formatNumber(1.00), "1.00", "Show all the significant decimal digits.");
	formatter.setDecimalVisibility(DecimalVisibility.MINIMUM_ONE_DIGIT);
	strictEqual(formatter.formatNumber(1.10), "1.1", "Show at least one decimal digits.");
	formatter.setDecimalVisibility(DecimalVisibility.STRIP_TRAILING_ZEROS);
	strictEqual(formatter.formatNumber(1.10), "1.1", "Strip the trailing zeros from a decimal number.");
	strictEqual(formatter.formatNumber(1), "1", "Strip the trailing zeros from an integer.");
	formatter.setDecimalVisibility(DecimalVisibility.FULL_PRECISION);
	
	// decimal precision
	formatter.setDecimalPrecision(4);
	strictEqual(formatter.formatNumber(1.1234), "1.1234", "Set the decimal precision.");
	formatter.setDecimalPrecision(2);
	
	// decimal delimiter
	formatter.setDecimalDelimiter("x");
	strictEqual(formatter.formatNumber(1.0), "1x00", "Set the decimal delimiter.");
	formatter.setDecimalDelimiter(".");
	
	// leading zero
	formatter.enableLeadingZero(true);
	strictEqual(formatter.formatNumber(0.1), "0.10", "Enable the leading zeros.");
	formatter.enableLeadingZero(false);
	strictEqual(formatter.formatNumber(0.1), ".10", "Disable the leading zeros.");
	formatter.setDecimalPrecision(0);
	strictEqual(formatter.formatNumber(0), "0", "Format a zero with no leading zero or decimals.");
	formatter.setDecimalPrecision(2);
	formatter.enableLeadingZero(true);
	
	// group size
	formatter.setGroupSize(4);
	strictEqual(formatter.formatNumber(1234567890), "12,3456,7890.00", "Set the group size.");
	formatter.setGroupSize(3);
	
	// group delimiter
	formatter.setGroupDelimiter("x");
	strictEqual(formatter.formatNumber(1234567890), "1x234x567x890.00", "Set the group delimiter.");
	formatter.setGroupDelimiter(",");
	
	// negative affixes
	formatter.setNegativePrefix("x");
	strictEqual(formatter.formatNumber(-1), "x1.00", "Set the negative prefix.");
	formatter.setNegativePrefix("");
	formatter.setNegativeSuffix("x");
	strictEqual(formatter.formatNumber(-1), "1.00x", "Set the negative suffix.");
	formatter.setNegativePrefix("-");
	formatter.setNegativeSuffix("");
	
	// decimal rounding
	formatter.setDecimalPrecision(1);
	formatter.setRoundingCallback(Math.round);
	strictEqual(formatter.formatNumber(0.05), "0.1", "Round a decimal number using regular rounding.");
	strictEqual(formatter.formatNumber(-0.15), "-0.1", "Round a negative decimal number using regular rounding.");
	formatter.setRoundingCallback(Math.ceil);
	strictEqual(formatter.formatNumber(0.04), "0.1", "Round a decimal number using ceiling.");
	strictEqual(formatter.formatNumber(-0.14), "-0.1", "Round a negative decimal number using ceiling.");
	formatter.setRoundingCallback(Math.floor);
	strictEqual(formatter.formatNumber(0.16), "0.1", "Round a decimal number using flooring.");
	strictEqual(formatter.formatNumber(-0.06), "-0.1", "Round a negative decimal number using flooring.");
	formatter.setRoundingCallback(Math.round);
	formatter.setDecimalPrecision(2);
	
	// integer rounding
	formatter.setDecimalPrecision(0);
	formatter.setRoundingCallback(Math.round);
	strictEqual(formatter.formatNumber(0.5), "1", "Round a number to an integer using regular rounding.");
	strictEqual(formatter.formatNumber(-1.5), "-1", "Round a negative number to an integer using regular rounding.");
	formatter.setRoundingCallback(Math.ceil);
	strictEqual(formatter.formatNumber(0.4), "1", "Round a number to an integer using ceiling.");
	strictEqual(formatter.formatNumber(-1.4), "-1", "Round a negative number to an integer using ceiling.");
	formatter.setRoundingCallback(Math.floor);
	strictEqual(formatter.formatNumber(1.6), "1", "Round a number to an integer using flooring.");
	strictEqual(formatter.formatNumber(-0.6), "-1", "Round a negative number to an integer using flooring.");
	formatter.setRoundingCallback(Math.round);
	formatter.setDecimalPrecision(2);
}
